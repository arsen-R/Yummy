package com.example.recipeapp.data.mapper

import com.example.recipeapp.data.network.dto.RecipeDto
import com.example.recipeapp.domain.mapper.BidirectionalMapper
import com.example.recipeapp.domain.model.Recipe

class RecipeMapper(
    private val brandMapper: BrandMapper,
    private val compilationMapper: CompilationMapper,
    private val creditMapper: CreditMapper,
    private val instructionMapper: InstructionMapper,
    private val nutritionMapper: NutritionMapper,
    private val priceMapper: PriceMapper,
    private val renditionMapper: RenditionMapper,
    private val sectionMapper: SectionMapper,
    private val showMapper: ShowMapper,
    private val tagMapper: TagMapper,
    private val topicMapper: TopicMapper,
    private val userRatingsMapper: UserRatingsMapper,
    private val totalTimeTierMapper: TotalTimeTierMapper,
): BidirectionalMapper<Recipe?, RecipeDto?> {
    override fun fromDomain(value: Recipe?): RecipeDto {
        return RecipeDto(
            approved_at = value?.approved_at,
            aspect_ratio = value?.aspect_ratio,
            beauty_url = value?.beauty_url,
            buzz_id = value?.buzz_id,
            brand = brandMapper.fromDomain(value?.brand),
            canonical_id = value?.canonical_id,
            compilations = value?.compilations?.map { compilationMapper.fromDomain(it) },
            cook_time_minutes = value?.cook_time_minutes,
            country = value?.country,
            created_at = value?.created_at,
            credits = value?.credits?.map { creditMapper.fromDomain(it) },
            description = value?.description,
            draft_status = value?.draft_status,
            id = value?.id,
            inspired_by_url = value?.inspired_by_url,
            instructions = value?.instructions?.map { instructionMapper.fromDomain(it) },
            is_one_top = value?.is_one_top,
            is_shoppable = value?.is_shoppable,
            keywords = value?.keywords,
            language = value?.language,
            name = value?.name,
            num_servings = value?.num_servings,
            nutrition = nutritionMapper.fromDomain(value?.nutrition),
            nutrition_visibility = value?.nutrition_visibility,
            original_video_url = value?.original_video_url,
            prep_time_minutes = value?.prep_time_minutes,
            price = priceMapper.fromDomain(value?.price),
            promotion = value?.promotion,
            renditions = value?.renditions?.map { renditionMapper.fromDomain(it) },
            sections = value?.sections?.map { sectionMapper.fromDomain(it) },
            seo_path = value?.seo_path,
            seo_title = value?.seo_title,
            servings_noun_plural = value?.servings_noun_plural,
            servings_noun_singular = value?.servings_noun_singular,
            show = showMapper.fromDomain(value?.show),
            show_id = value?.show_id,
            slug = value?.slug,
            tags = value?.tags?.map { tagMapper.fromDomain(it) },
            thumbnail_alt_text = value?.thumbnail_alt_text,
            thumbnail_url = value?.thumbnail_url,
            tips_and_ratings_enabled = value?.tips_and_ratings_enabled,
            topics = value?.topics?.map { topicMapper.fromDomain(it) },
            total_time_minutes = value?.total_time_minutes,
            total_time_tier = totalTimeTierMapper.fromDomain(value?.total_time_tier),
            updated_at = value?.updated_at,
            user_ratings = userRatingsMapper.fromDomain(value?.user_ratings),
            video_ad_content = value?.video_ad_content,
            video_id = value?.video_id,
            video_url = value?.video_url,
            yields = value?.yields,
        )
    }

    override fun toDomain(value: RecipeDto?): Recipe {
        return Recipe(
            approved_at = value?.approved_at,
            aspect_ratio = value?.aspect_ratio,
            beauty_url = value?.beauty_url,
            buzz_id = value?.buzz_id,
            brand = brandMapper.toDomain(value?.brand),
            canonical_id = value?.canonical_id,
            compilations = value?.compilations?.map { compilationMapper.toDomain(it) },
            cook_time_minutes = value?.cook_time_minutes,
            country = value?.country,
            created_at = value?.created_at,
            credits = value?.credits?.map { creditMapper.toDomain(it) },
            description = value?.description,
            draft_status = value?.draft_status,
            id = value?.id,
            inspired_by_url = value?.inspired_by_url,
            instructions = value?.instructions?.map { instructionMapper.toDomain(it) },
            is_one_top = value?.is_one_top,
            is_shoppable = value?.is_shoppable,
            keywords = value?.keywords,
            language = value?.language,
            name = value?.name,
            num_servings = value?.num_servings,
            nutrition = nutritionMapper.toDomain(value?.nutrition),
            nutrition_visibility = value?.nutrition_visibility,
            original_video_url = value?.original_video_url,
            prep_time_minutes = value?.prep_time_minutes,
            price = priceMapper.toDomain(value?.price),
            promotion = value?.promotion,
            renditions = value?.renditions?.map { renditionMapper.toDomain(it) },
            sections = value?.sections?.map { sectionMapper.toDomain(it) },
            seo_path = value?.seo_path,
            seo_title = value?.seo_title,
            servings_noun_plural = value?.servings_noun_plural,
            servings_noun_singular = value?.servings_noun_singular,
            show = showMapper.toDomain(value?.show),
            show_id = value?.show_id,
            slug = value?.slug,
            tags = value?.tags?.map { tagMapper.toDomain(it) },
            thumbnail_alt_text = value?.thumbnail_alt_text,
            thumbnail_url = value?.thumbnail_url,
            tips_and_ratings_enabled = value?.tips_and_ratings_enabled,
            topics = value?.topics?.map { topicMapper.toDomain(it) },
            total_time_minutes = value?.total_time_minutes,
            total_time_tier = totalTimeTierMapper.toDomain(value?.total_time_tier),
            updated_at = value?.updated_at,
            user_ratings = userRatingsMapper.toDomain(value?.user_ratings),
            video_ad_content = value?.video_ad_content,
            video_id = value?.video_id,
            video_url = value?.video_url,
            yields = value?.yields,
        )
    }
}